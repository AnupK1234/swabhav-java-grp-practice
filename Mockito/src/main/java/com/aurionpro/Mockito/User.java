package com.aurionpro.Mockito;

public class User {
	private int id;
	private String name;

	// Constructors, getters, setters
	public User(int id, String name) {
		this.id = id;
		this.name = name;
	}

	// getters
	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}
}
